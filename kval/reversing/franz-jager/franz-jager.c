uint32_t rot_word(uint32_t w) { return (w >> 8) | ((w & 0xff) << 24); }

uint32_t sub_word(uint32_t w) {
    return (
        (uint32_t)S_BOX[w & 0xff] |
        ((uint32_t)S_BOX[(w >> 8) & 0xff]) << 8 |
        ((uint32_t)S_BOX[(w >> 16) & 0xff]) << 16 |
        ((uint32_t)S_BOX[(w >> 24) & 0xff]) << 24
    );
}

void key_schedule(const uint32_t *K, uint32_t *W) {
  int N = KEY_SIZE / 4;
  for (int i = 0; i < N; i++) {
    W[i] = K[i];
  }

  int R = ROUNDS + 1;
  for (int i = N; i < 4 * R; i++) {
    if (i % N == 0) {
      W[i] = W[i - N] ^ sub_word(rot_word(W[i - 1])) ^ rcon[i / N];
    } else if (N > 6 && i % N == 4) {
      W[i] = W[i - N] ^ sub_word(W[i - 1]);
    } else {
      W[i] = W[i - N] ^ W[i - 1];
    }
  }
}

void add_round_key(uint32_t *state, const uint32_t *round_keys, int round) {
  for (int i = 0; i < BLOCK_SIZE / 4; i++) {
    state[i] ^= round_keys[BLOCK_SIZE / 4 * round + i];
  }
}

void sub_bytes(uint32_t *state) {
  for (int i = 0; i < BLOCK_SIZE / 4; i++) {
    state[i] = sub_word(state[i]);
  }
}

void shift_rows(uint32_t *state) {
  uint32_t w0 = state[0];
  uint32_t w1 = state[1];
  uint32_t w2 = state[2];
  uint32_t w3 = state[3];
  state[0] = (w0 & 0xff) | (w1 & 0xff00) | (w2 & 0xff0000) | (w3 & 0xff000000);
  state[1] = (w1 & 0xff) | (w2 & 0xff00) | (w3 & 0xff0000) | (w0 & 0xff000000);
  state[2] = (w2 & 0xff) | (w3 & 0xff00) | (w0 & 0xff0000) | (w1 & 0xff000000);
  state[3] = (w3 & 0xff) | (w0 & 0xff00) | (w1 & 0xff0000) | (w2 & 0xff000000);
}

uint32_t mix_column(uint32_t col) {
    return (
        (MUL_2[col & 0xff] ^ MUL_3[(col >> 8) & 0xff] ^ ((col >> 16) & 0xff) ^ ((col >> 24) & 0xff)) |
        ((col & 0xff) ^ MUL_2[(col >> 8) & 0xff] ^ MUL_3[(col >> 16) & 0xff] ^ ((col >> 24) & 0xff)) << 8 |
        ((col & 0xff) ^ ((col >> 8) & 0xff) ^ MUL_2[(col >> 16) & 0xff] ^ MUL_3[(col >> 24) & 0xff]) << 16 |
        (MUL_3[col & 0xff] ^ ((col >> 8) & 0xff) ^ ((col >> 16) & 0xff) ^ MUL_2[(col >> 24) & 0xff]) << 24
    );
}

void mix_columns(uint32_t *state) {
  for (int i = 0; i < BLOCK_SIZE / 4; i++) {
    state[i] = mix_column(state[i]);
  }
}

// Actually aes encrypt
void funky_decrypt(const uint32_t *round_keys, uint32_t *state) {
  add_round_key(state, round_keys, 0);
  for (int round = 1; round < ROUNDS; round++) {
    sub_bytes(state);
    shift_rows(state);
    mix_columns(state);
    add_round_key(state, round_keys, round);
  }
  sub_bytes(state);
  shift_rows(state);
  add_round_key(state, round_keys, ROUNDS);
}

