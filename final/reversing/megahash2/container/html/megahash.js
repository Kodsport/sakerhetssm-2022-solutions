let mul = (a, b) => {
    let p = 0;
    while(a != 0 && b != 0) {
        if(b & 1) p ^= a;
        if(a & 0x80) a = (a << 1) ^ M;
        else a <<= 1;
        b >>= 1;
    }
    return p;
}

